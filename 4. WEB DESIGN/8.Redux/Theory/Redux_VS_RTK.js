// React-Redux :-

// • Purpose           : Primarily focuses on integrating React with Redux.
// • Connect Function  : Requires the use of the connect function to connect React components with the Redux store.
// • Boilerplate       : Involves writing more boilerplate code, especially when dealing with actions and reducers.

// • હેતુ                : મુખ્યત્વે Redux સાથે પ્રતિક્રિયાને એકીકૃત કરવા પર ધ્યાન કેન્દ્રિત કરે છે.
// • કનેક્ટ ફંક્શન         : Redux સ્ટોર સાથે પ્રતિક્રિયા ઘટકોને કનેક્ટ કરવા માટે કનેક્ટ ફંક્શનનો ઉપયોગ જરૂરી છે.
// • બોઈલરપ્લેટ          : વધુ બોઈલરપ્લેટ કોડ લખવાનો સમાવેશ થાય છે, ખાસ કરીને જ્યારે ક્રિયાઓ અને રીડ્યુસર સાથે કામ કરો.


// Redux Toolkit :-

// • Purpose                : Redux Toolkit simplifies Redux development and reduces boilerplate.
// • createSlice            : Introduces a concise way to define reducers & actions.
// • configureStore         : Provides an easy setup for the store with common middleware and options.
// • Immutability Helpers   : Offers utility functions for working with immutable data structures.
// • Automatic Immutability : createSlice automatically handles immutability concerns.

// • હેતુ                     : Redux Toolkit Redux વિકાસને સરળ બનાવે છે અને બોઈલરપ્લેટ ઘટાડે છે.
// • CreateSlice            : રીડ્યુસર અને ક્રિયાઓને વ્યાખ્યાયિત કરવાની સંક્ષિપ્ત રીત રજૂ કરે છે.
// • configureStore         : સામાન્ય મિડલવેર અને વિકલ્પો સાથે સ્ટોર માટે સરળ સેટઅપ પ્રદાન કરે છે.
// • અપરિવર્તનક્ષમતા સહાયક    : અપરિવર્તનશીલ ડેટા સ્ટ્રક્ચર્સ સાથે કામ કરવા માટે ઉપયોગિતા કાર્યો પ્રદાન કરે છે.
// • સ્વચાલિત પરિવર્તનક્ષમતા    : createSlice આપમેળે અપરિવર્તનશીલતાની ચિંતાઓને સંભાળે છે.



// Key Differences :-

// 1.Focus :---
// • React-Redux is more about the integration of React & Redux.
//   React-Redux એ React અને Redux ના એકીકરણ વિશે વધુ છે.

// • Redux Toolkit is a set of tools to enhance Redux development.
//   Redux Toolkit Redux વિકાસને વધારવા માટેના સાધનોનો સમૂહ છે. 

// 2.Abstraction :---
// • Redux Toolkit provides the higher-level abstractions like createSlice, aiming to reduce manual configuration & boilerplate code
//   Redux Toolkit મેન્યુઅલ કન્ફિગરેશન અને બોઈલરપ્લેટ કોડ ઘટાડવાનું લક્ષ્ય રાખીને CreateSlice જેવા ઉચ્ચ-સ્તરના એબ્સ્ટ્રેક્શન્સ પ્રદાન કરે છે.

// 3. Development Speed :---
// • Redux Toolkit can lead to faster development by streamlining common tasks,
//   Redux Toolkit સામાન્ય કાર્યોને સુવ્યવસ્થિત કરીને ઝડપી વિકાસ તરફ દોરી શકે છે, 

// • React-Redux is essential for connecting React components with Redux state.
