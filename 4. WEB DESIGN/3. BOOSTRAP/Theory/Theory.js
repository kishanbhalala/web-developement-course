// What is BootStrap?

// Bootstrap is a free and open-source cSS framework for developing responsive, mobile-first websites and web applications.
// બુટસ્ટ્રેપ એ પ્રતિભાવશીલ, મોબાઇલ-પ્રથમ વેબસાઇટ્સ અને વેબ એપ્લિકેશન્સ વિકસાવવા માટે એક મફત અને ઓપન સોર્સ સીએસએસ ફ્રેમવર્ક છે.

// Bootstrap is based on a 12-column grid system that makes it easy to create responsive layouts that adapt to different screen sizes.
// બુટસ્ટ્રેપ 12-કૉલમ ગ્રીડ સિસ્ટમ પર આધારિત છે જે વિવિધ સ્ક્રીન માપોને અનુરૂપ પ્રતિભાવ લેઆઉટ બનાવવાનું સરળ બનાવે છે.

// It also includes a wide range of pre-built components, such as buttons, forms, navigation bars, and modals, that can be used to quickly and easily create complex web pages.
// તેમાં બટનો, ફોર્મ્સ, નેવિગેશન બાર અને મોડલ્સ જેવા પૂર્વ-બિલ્ટ ઘટકોની વિશાળ શ્રેણીનો પણ સમાવેશ થાય છે, જેનો ઉપયોગ જટિલ વેબ પૃષ્ઠો ઝડપથી અને સરળતાથી બનાવવા માટે થઈ શકે છે.



// Key Features :-

// • Responsive :-  Adapts layouts to diverse screen sizes.
// • Mobile-first :-  Prioritizes mobile device design.
// • User-friendly :-  Easy for beginners with comprehensive documentation and community support.
// • Customizable :-  Highly adaptable to specific development needs.
// • Diverse Components :-  Includes buttons, forms, navigation bars, and modals for varied functionality.

// • રિસ્પોન્સિવ :- વિવિધ સ્ક્રીન માપો માટે લેઆઉટને અપનાવે છે.
// • મોબાઇલ-પ્રથમ :- મોબાઇલ ઉપકરણ ડિઝાઇનને પ્રાથમિકતા આપે છે.
// • વપરાશકર્તા મૈત્રીપૂર્ણ:- વ્યાપક દસ્તાવેજીકરણ અને સમુદાય સમર્થન સાથે નવા નિશાળીયા માટે સરળ.
// • વૈવિધ્યપૂર્ણ: - ચોક્કસ વિકાસ જરૂરિયાતો માટે અત્યંત સ્વીકાર્ય.
// • વિવિધ ઘટકો :- વિવિધ કાર્યક્ષમતા માટે બટનો, ફોર્મ્સ, નેવિગેશન બાર અને મોડલ્સનો સમાવેશ થાય છે.


