// what is custom hook?


// A custom Hook is a JavaScript function whose name starts with "use". to remove the duplicated logic in components and we can exract that logic to custom hook.
// કસ્ટમ હૂક એ JavaScript ફંક્શન છે જેનું નામ "ઉપયોગ" થી શરૂ થાય છે. ઘટકોમાં ડુપ્લિકેટેડ લોજિક દૂર કરવા માટે અને અમે તે તર્કને કસ્ટમ હૂકમાં એક્સટ્રેક્ટ કરી શકીએ છીએ.

// We can use other hooks in custom hooks as well.
// અમે કસ્ટમ હુક્સમાં અન્ય હુક્સનો પણ ઉપયોગ કરી શકીએ છીએ.


// Benefits :
// • Encapsulates and organizes logic.
// • Promotes reusability across components.
// • Follows the same rules as React Hooks.